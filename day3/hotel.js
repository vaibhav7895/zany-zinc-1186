function filterFunc(hotelName,hdata){
  var filData = [];
  for(var i=0; i<hdata.length; i++) {
    hotelName = hotelName.toLowerCase();
    var filterName = hdata[i].hotelName.toLowerCase();
    if(filterName.includes(hotelName)) {
      filData.push(hdata[i]);
    }
  }
  return filData;
}
    
